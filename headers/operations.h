#include <iostream>
#include <cmath>

//////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Class that perform different math operations.
///
/// @addtogroup   MathOperations
//////////////////////////////////////////////////////////////////////////////////////////////////////
class MathOperations {
public:
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Function add two numbers
    /// @param[in] n1 first number
    /// @param[in] n2 second number
    /// 
    /// @return addition result
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    int add(int num1, int num2);

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Function subtract two numbers
    /// @param[in] n1 first number
    /// @param[in] n2 second number
    /// 
    /// @return subtraction result
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    int subtract(int num1, int num2);

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Function multiply two numbers
    /// @param[in] n1 first number
    /// @param[in] n2 second number
    /// 
    /// @return multiplycation result
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    int multiply(int num1, int num2);

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Function divide two numbers
    /// @param[in] n1 first number
    /// @param[in] n2 second number
    /// 
    /// @return division result
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    float divide(int num1, int num2);
};

//////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Class that perform advanced math operations.
///
/// @addtogroup   MathOperations
//////////////////////////////////////////////////////////////////////////////////////////////////////
class AdvancedMathOperations : public MathOperations {
public:
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Utilize multiple functions from the base class to perform an advanced operation
    /// @param[in] n1 first number
    /// @param[in] n2 second number 
    /// @param[in] n3 third number
    /// 
    /// @return result of performAdvancedOperation :  (num1 * num2) + (num2 - num3)
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    int performAdvancedOperation(int num1, int num2, int num3);

    //////////////////////////////////////////////////////////////////////////////////////////////////////
    /// @brief Calculate square root using Newton's method
    /// @param[in] x number to extract root from
    /// @param[in] epsilon epsilon value
    /// 
    /// @return square root of x
    //////////////////////////////////////////////////////////////////////////////////////////////////////
    double squareRoot(double x, double epsilon = 0.0001);
};